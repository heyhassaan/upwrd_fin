// PSX Market hours: Monday-Friday, 9:30 AM - 3:30 PM PKT (UTC+5)
export const isMarketOpen = (): boolean => {
  const now = new Date();
  const pktOffset = 5 * 60; // PKT is UTC+5
  const utc = now.getTime() + now.getTimezoneOffset() * 60000;
  const pktTime = new Date(utc + pktOffset * 60000);
  
  const day = pktTime.getDay();
  const hours = pktTime.getHours();
  const minutes = pktTime.getMinutes();
  const totalMinutes = hours * 60 + minutes;
  
  // Market is closed on weekends
  if (day === 0 || day === 6) return false;
  
  // Market hours: 9:30 AM (570 min) to 3:30 PM (930 min) PKT
  const marketOpen = 9 * 60 + 30; // 9:30 AM
  const marketClose = 15 * 60 + 30; // 3:30 PM
  
  return totalMinutes >= marketOpen && totalMinutes <= marketClose;
};

export const getMarketStatus = (): { isOpen: boolean; message: string } => {
  const open = isMarketOpen();
  return {
    isOpen: open,
    message: open ? "Market Open" : "Market Closed",
  };
};

export const formatCurrency = (value: number): string => {
  return new Intl.NumberFormat('en-PK', {
    style: 'currency',
    currency: 'PKR',
    minimumFractionDigits: 2,
  }).format(value);
};

export const formatNumber = (value: number): string => {
  return new Intl.NumberFormat('en-PK').format(value);
};
