// Helper to generate random chart data
const generateChartData = (basePrice: number, isPositive: boolean): number[] => {
  const data: number[] = [];
  let price = basePrice * (isPositive ? 0.97 : 1.03);
  for (let i = 0; i < 10; i++) {
    const change = (Math.random() - 0.5) * basePrice * 0.02;
    price += isPositive ? Math.abs(change) * 0.3 : -Math.abs(change) * 0.3;
    data.push(price);
  }
  data.push(basePrice);
  return data;
};

